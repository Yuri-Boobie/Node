const tarefas = [
    {id: 1, titulo: "Estudar JavaScript", concluida: false},
    {id: 2, titulo: "Fazer compras", concluida: true},
]; //Array = Valor

function listarTarefas() {
    tarefas.forEach(function (tarefa) {
        console.log(
            "#" + tarefa.id +
             " - " +
              tarefa.titulo +
              " - Concluido: " +
               tarefa.concluida
        );
    });

    console.log(`#${tarefa.id} - ${tarefa.titulo} - Concluido: ${tarefa.concluida}`);
}

function criarTarefa(titulo) {
    const novaTarefa = {
        id: tarefas.length + 1,
        titulo: titulo,
        concluida: false,
    };
    tarefas.push(novaTarefa);
    console.log("Tarefa adicionada com sucesso!");
}

console.log("Lista de Tarefas:");
listarTarefas();

console.log("\nAdicionando uma nova tarefa...");
criarTarefa("Planejar Figma TCC");

console.log("\nLista de Tarefas Atualizada:");
listarTarefas();



























































































































































































































































































































































































































































































































































































































































